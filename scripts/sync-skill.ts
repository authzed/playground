import { readFileSync, readdirSync, writeFileSync } from "fs";
import { homedir } from "os";
import path from "path";

const SKILL_DIR =
  process.env.SKILL_SRC_DIR ??
  path.join(
    homedir(),
    ".claude-zed/plugins/marketplaces/authzed-marketplace/app-permissions-dev/skills/spicedb-schema-design",
  );

const references = {
  patterns: "references/patterns.md",
  "anti-patterns": "references/anti-patterns.md",
  "schema-evolution": "references/schema-evolution.md",
} as const;

function read(rel: string): string {
  return readFileSync(path.join(SKILL_DIR, rel), "utf8");
}

const overview = read("SKILL.md");
const refEntries = Object.entries(references).map(
  ([name, rel]) => `  ${JSON.stringify(name)}: ${JSON.stringify(read(rel))},`,
);
// "examples" is a concatenation of the example schema files for quick reference.
const examplesDir = path.join(SKILL_DIR, "examples");
const examples = readdirSync(examplesDir)
  .filter((f) => f.endsWith(".zed") || f.endsWith(".yaml"))
  .sort()
  .map((f) => `## ${f}\n\n${readFileSync(path.join(examplesDir, f), "utf8")}`)
  .join("\n\n---\n\n");

const out = `// GENERATED by scripts/sync-skill.ts — do not edit by hand.
export type SkillReferenceName = "patterns" | "anti-patterns" | "schema-evolution" | "examples";

export const SKILL_OVERVIEW = ${JSON.stringify(overview)};

export const SKILL_REFERENCES: Record<SkillReferenceName, string> = {
${refEntries.join("\n")}
  "examples": ${JSON.stringify(examples)},
};
`;

writeFileSync(path.join(process.cwd(), "api/_skill/content.ts"), out);
console.log("Wrote api/_skill/content.ts");
